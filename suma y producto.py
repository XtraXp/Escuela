# Escribir un programa en el cual se ingresen cuatro números,
# calcular e informar la suma de los dos primeros y
# el producto del tercero y el cuarto.
n1=int(input("Escribe número 1:"))
n2=int(input("Escribe número 2:"))
n3=int(input("Escribe número 3:"))
n4=int(input("Escribe número 4:"))
suma=n1+n2
producto=n3*n4
print("La suma entre el primer y segundo número es de", suma)
print("EL producto entre el tercer y cuarto número es de", producto)
